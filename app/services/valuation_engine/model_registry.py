def model_row(model: str, applicability: str, reason: str, weight: str) -> dict[str, str]:
    definition = MODEL_REGISTRY.get(model) or MODEL_REGISTRY.get("DCF项目版")
    if definition is None:
        return {
            "模型": model,
            "适用度": applicability,
            "适用原因": reason,
            "需要的数据": "待补充",
            "必需数据": "待补充",
            "输出结果": "待补充",
            "权重建议": weight,
        }
    return {
        "模型": definition.model,
        "适用度": applicability,
        "适用原因": reason,
        "需要的数据": definition.required_data,
        "必需数据": definition.required_data,
        "输出结果": definition.output,
        "权重建议": weight,
    }


def dedupe(items: list[str]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for item in items:
        if item and item not in seen:
            seen.add(item)
            result.append(item)
    return result
